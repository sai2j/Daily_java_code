package basic;

public class sum_of_divisior {
          public static void main(String[] args) {
                    int num =6;
                    int sum =0;
                    for(int i =num; i<=num ;i++){
                              if(num %i ==0){
                                        sum=sum+i;
                              }
                                    System.out.println(sum);
                    }
                    System.out.println(sum);
          }
}
